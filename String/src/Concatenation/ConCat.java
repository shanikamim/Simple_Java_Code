package Concatenation;

public class ConCat {
	
	
	public static void main(String[] args) {
		String s1  = "This ";
		String s2 = "is ";
		String s3 = s1.concat(s2);
		System.out.println(s3);
		
		String s4 = "koushik";
		String s5 = s3.concat(s4);
		
		System.out.println(s5);
		
		String s6 = 50 + 50 +"Hello" + 40 + 40;
		System.out.println(s6);
	}
	
	
	
}
